#!/usr/bin/env node
/**
 * Scans each gateway/connectors/<provider>/manifest.json and generates a typed TypeScript
 * module the UI can import directly.
 *
 * Output is checked in so local dev and Docker builds don't need the gateway
 * directory in the build context. Re-run when connector manifests change.
 *
 * Usage: node scripts/build-source-manifests.mjs
 */
import { existsSync, readFileSync, writeFileSync, readdirSync } from "node:fs";
import { resolve, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const MONOREPO_ROOT = resolve(__dirname, "..");
const CONNECTORS_DIR = resolve(MONOREPO_ROOT, "gateway", "connectors");
const OUTPUT = resolve(
  MONOREPO_ROOT,
  "apps",
  "ui",
  "src",
  "lib",
  "sources",
  "generated-manifests.ts"
);

if (!existsSync(CONNECTORS_DIR)) {
  if (existsSync(OUTPUT)) {
    console.log(
      "[source-manifests] Connectors dir not present (docker build?); " +
        "using committed generated-manifests.ts."
    );
    process.exit(0);
  }
  console.error(
    `[source-manifests] Connectors dir missing at ${CONNECTORS_DIR} AND no ` +
      `committed output at ${OUTPUT}. Run this script from the monorepo root.`
  );
  process.exit(1);
}

const REQUIRED_FIELDS = ["id", "name", "description", "icon", "auth"];

function validate(manifest, slug) {
  const missing = REQUIRED_FIELDS.filter((f) => !manifest[f]);
  if (missing.length > 0) {
    console.warn(
      `[source-manifests] Skipped ${slug}: missing ${missing.join(", ")}`
    );
    return false;
  }
  if (manifest.id !== slug) {
    console.warn(
      `[source-manifests] Skipped ${slug}: manifest.id "${manifest.id}" does not match directory name`
    );
    return false;
  }
  if (!manifest.auth.fields || !Array.isArray(manifest.auth.fields)) {
    console.warn(
      `[source-manifests] Skipped ${slug}: auth.fields must be an array`
    );
    return false;
  }
  return true;
}

function main() {
  const entries = readdirSync(CONNECTORS_DIR, { withFileTypes: true });
  const manifests = {};

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    if (entry.name.startsWith("_")) continue;

    const slug = entry.name;
    const manifestPath = join(CONNECTORS_DIR, slug, "manifest.json");
    let raw;
    try {
      raw = readFileSync(manifestPath, "utf8");
    } catch {
      continue;
    }

    let manifest;
    try {
      manifest = JSON.parse(raw);
    } catch (e) {
      console.warn(`[source-manifests] Skipped ${slug}: ${e.message}`);
      continue;
    }

    if (!validate(manifest, slug)) continue;
    manifests[slug] = manifest;
  }

  const ids = Object.keys(manifests).sort();

  const output = `// Auto-generated by scripts/build-source-manifests.mjs
// Do not edit by hand. Re-run: node scripts/build-source-manifests.mjs

export interface ProviderCredentialField {
  key: string;
  label: string;
  placeholder: string;
  input_type: "password" | "text" | "textarea" | "file";
  required: boolean;
}

export interface ProviderSetupStep {
  title: string;
  description: string;
  link?: { label: string; url: string };
}

export interface ProviderOAuthConfig {
  authorize_url: string;
  token_url: string;
  token_field: string;
  scopes: string[];
  env_client_id: string;
  env_client_secret: string;
  auth_method: "basic" | "post_body";
  extra_params?: Record<string, string>;
  response_mapping: Record<string, string>;
}

export interface ProviderManifest {
  id: string;
  name: string;
  description: string;
  icon: string;
  item_label?: string;
  auth: {
    type: "api_key" | "oauth" | "service_account" | "token_pair";
    fields: ProviderCredentialField[];
    oauth?: ProviderOAuthConfig;
    setup_steps: ProviderSetupStep[];
  };
  source_url_template?: string;
  supports_write: boolean;
}

export const PROVIDER_MANIFESTS: Record<string, ProviderManifest> = ${JSON.stringify(
    manifests,
    null,
    2
  )} as const;

export const PROVIDER_IDS: string[] = ${JSON.stringify(ids)};
`;

  writeFileSync(OUTPUT, output);
  console.log(
    `[source-manifests] Wrote ${ids.length} manifest(s) to ${OUTPUT.replace(
      MONOREPO_ROOT + "/",
      ""
    )}: ${ids.join(", ")}`
  );
}

main();
