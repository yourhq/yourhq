#!/usr/bin/env node
/**
 * Walks the monorepo's templates/ directory, reads each agent.json, and
 * emits src/generated/templates.ts with a typed array the UI can import
 * directly.
 *
 * Runs before `next build`. The output is checked in so local dev doesn't
 * need to re-run the script for every edit — just re-run when templates
 * change.
 */
import { existsSync, readFileSync, writeFileSync, readdirSync } from "node:fs";
import { resolve, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const UI_ROOT = resolve(__dirname, "..");
const MONOREPO_ROOT = resolve(UI_ROOT, "..", "..");
const TEMPLATES_DIR = resolve(MONOREPO_ROOT, "templates");
const OUTPUT = resolve(UI_ROOT, "src", "generated", "templates.ts");

// When this script runs inside the UI Docker image (build stage), the
// monorepo's templates/ directory isn't part of the build context — only
// apps/ui/ is. That's intentional: the committed src/generated/templates.ts
// already has everything the image needs. Skip regeneration in that case.
if (!existsSync(TEMPLATES_DIR)) {
  if (existsSync(OUTPUT)) {
    console.log(
      "[templates-index] Templates dir not present (docker build?); " +
        "using committed src/generated/templates.ts."
    );
    process.exit(0);
  }
  console.error(
    `[templates-index] Templates dir missing at ${TEMPLATES_DIR} AND no ` +
      `committed output at ${OUTPUT}. Run this script from the monorepo root ` +
      `before building.`
  );
  process.exit(1);
}

function main() {
  const entries = readdirSync(TEMPLATES_DIR, { withFileTypes: true });
  const templates = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const slug = entry.name;
    // `default` is the base template every agent forks from; not a
    // user-facing choice.
    if (slug === "default") continue;
    const agentJsonPath = join(TEMPLATES_DIR, slug, "agent.json");
    let raw;
    try {
      raw = readFileSync(agentJsonPath, "utf8");
    } catch {
      continue;
    }
    let manifest;
    try {
      manifest = JSON.parse(raw);
    } catch (e) {
      console.warn(`[templates-index] Skipped ${slug}: ${e.message}`);
      continue;
    }
    templates.push({
      branch: `template/${slug}`,
      name: manifest.name || slug,
      description: manifest.description || "",
      emoji: manifest.emoji,
      team: manifest.team,
      domains: manifest.domains,
      capabilities: manifest.capabilities,
    });
  }

  templates.sort((a, b) => a.name.localeCompare(b.name));

  const fileHeader =
    `// Auto-generated by apps/ui/scripts/build-templates-index.mjs\n` +
    `// Do not edit by hand. Re-run: npm run build:templates\n\n` +
    `import type { AgentTemplate } from "@/lib/agents/types";\n\n`;

  const body = `export const BUNDLED_TEMPLATES: AgentTemplate[] = ${JSON.stringify(
    templates,
    null,
    2
  )};\n`;

  writeFileSync(OUTPUT, fileHeader + body);
  console.log(
    `[templates-index] Wrote ${templates.length} templates to ${OUTPUT.replace(
      MONOREPO_ROOT + "/",
      ""
    )}`
  );
}

main();
